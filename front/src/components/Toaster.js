import { toast } from 'react-toastify';

export function ToastSuccess(msg){
    toast.success(`Success! ${msg}`, {
        position: "top-right",
        autoClose: 5000,
        hideProgressBar: false,
        closeOnClick: true,
        pauseOnHover: true,
        draggable: true,
        progress: undefined,
    });
}

